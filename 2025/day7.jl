function nextstep(input::String)::String
	working::Vector{Char} = collect(input) #bc we cant replace in-place in strings
	index    = findall("S", input)[begin].start
	linessep = split(input, "\n")
	linewidth = length(linessep[1]) + 1 # accounting for the removed \n
	
	if working[end] == '|' #if the last element is | then its definitely completed
		return input
	end

	#println(repr(below(working, index)))
	if below(working, index) == '.'
		#println()
		working[ibelow(working, index)] = '|'

		return join(working, "") 
	end

	for i in findall('|', input)
		#printstyled("$i\n", color=:red)
		if below(working, i) == '|'
			continue
		elseif below(working, i) == '.'
			working[ibelow(working, i)] = '|'
		elseif below(working, i)  == '^'
			working[(ibelow(working, i) -1)] = '|'
			working[(ibelow(working, i) +1)] = '|'
		end
	end
	return join(working, "")
end

function countthesplits(input::String)::Int
	counter = 0
	for (i, j) in enumerate(input)
		if i > length(input) - width
			break
		end
		if j == '|' && below(input, i) == '^'
			counter += 1
		end
	end
	counter
end


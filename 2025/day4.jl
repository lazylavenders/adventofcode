function convolution(A::Matrix, B::Matrix; larger=true)::Matrix{Integer}
	"Here, A must be the larger matrix, B 3x3 matrix. `larger` gives the padding"
	sizeA = size(A)
	res = zeros(sizeA[1] + 2, sizeA[2] + 2)
	for i = 1 : sizeA[1]
		for j = 1 : sizeA[2]
			value::Matrix = A[i, j]::Number * B
			for q = -1:1
				for w = -1:1
					res[(i+q+1),(w+j+1)] += value[(q + 2), (w + 2)]
				end
			end
		end
	end
	if larger return res
	else return res[begin+1:end-1, begin+1:end-1];end
end


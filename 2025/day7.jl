function mycrawler(input)
	lines = split(input, '\n')
	init = findall('S', lines[begin])
	register = zeros(Int, length(lines[begin]))
	register[init] .= 1
	splits = 0

	for i in 2:length(lines)
		splitters = findall('^', lines[i])
		yesbeams = findall(!iszero, register)

		for beam in yesbeams
			if beam in splitters
				register[beam - 1] += 1 * register[beam]
				register[beam + 1] += 1 * register[beam]
				register[beam]      -= 1 * register[beam]
				splits += 1
			end
		end
	end
	return sum(register), splits
end

